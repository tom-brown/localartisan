class UsersController < ApplicationController
  # GET /users
  # GET /users.json
  
  before_filter :require_admin, :only => :index
  before_filter :require_new_user, :only => :new
  
  def index
    @users = User.all

    respond_to do |format|
      format.html # index.html.erb
      format.json { render :json => @users }
    end
  end

  # GET /users/1
  # GET /users/1.json
